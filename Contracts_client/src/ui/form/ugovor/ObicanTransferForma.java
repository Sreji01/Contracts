    private void jButtonZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZapamtiActionPerformed
        try {
            String warningString = "";
            stavkeUgovora = mtsu.getStavkeUgovora();
            Igrac igrac = (Igrac) jComboBoxIgraci.getSelectedItem();
            if (igrac == null) {
                warningString += "Igrac mora biti izabran!" + "\n";
            }
            Object odlazeciKlub = jComboBoxNapusteniKlubovi.getSelectedItem();
            if (odlazeciKlub == null) {
                warningString += "Napusteni klub mora biti izabran!" + "\n";
            }
            Object dolazeciKlub = jComboBoxPridruzeniKlubovi.getSelectedItem();
            if (dolazeciKlub == null) {
                warningString += "Pridruzeni klub mora biti izabran!" + "\n";
            }
            if (igrac == null || odlazeciKlub == null || dolazeciKlub == null) {
                JOptionPane.showMessageDialog(this, warningString, "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Klub napusteniKlub;
            Klub pridruzeniKlub;
            if (odlazeciKlub.equals("--Slobodan igrac--")) {
                napusteniKlub = new Klub();
                napusteniKlub.setNaziv("--Slobodan igrac--");
            } else {
                napusteniKlub = (Klub) odlazeciKlub;
            }
            if (dolazeciKlub.equals("--Slobodan igrac--")) {
                pridruzeniKlub = new Klub();
                pridruzeniKlub.setNaziv("--Slobodan igrac--");
            } else {
                pridruzeniKlub = (Klub) dolazeciKlub;
            }
            Ugovor ugovor = new Ugovor(-1, igrac, napusteniKlub, pridruzeniKlub, stavkeUgovora);
            Kontroller.getInstance().zapamtiUgovor(ugovor);
            uf.postaviKoloneTabeleUgovor();
            uf.postaviUkupanBrojUgovora();
            isprazniPolja();
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio ugovor!", "Potvrda", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti ugovor!" /*+ ex.getMessage()*/, "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonZapamtiActionPerformed

