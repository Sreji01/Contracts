    private void popuniComboBoxIgracima() throws Exception {
        List<Igrac> igraci = Kontroller.getInstance().ucitajListuIgraca();
        Collections.sort(igraci);
        jComboBoxIgraci.setModel(new DefaultComboBoxModel<>(igraci.toArray()));
        jComboBoxIgraci.setSelectedItem(null);
    }

    private void popuniComboBoxeveKlubovima() throws Exception {
        List<Klub> klubovi = Kontroller.getInstance().ucitajListuKlubova();
        Collections.sort(klubovi);
        jComboBoxNapusteniKlubovi.setModel(new DefaultComboBoxModel<>(klubovi.toArray()));
        jComboBoxNapusteniKlubovi.addItem("--Slobodan igrac--");
        jComboBoxNapusteniKlubovi.setSelectedItem(null);
        jComboBoxPridruzeniKlubovi.setModel(new DefaultComboBoxModel<>(klubovi.toArray()));
        jComboBoxPridruzeniKlubovi.addItem("--Slobodan igrac--");
        jComboBoxPridruzeniKlubovi.setSelectedItem(null);
    }

    private void postaviModelTabeleStavkaUgovora() {
        mtsu = new ModelTabeleStavkaUgovora(stavkeUgovora);
        mtsu.setOtf(this);
        jTableStavkeUgovora.setModel(mtsu);
        jTableStavkeUgovora.getColumnModel().getColumn(0).setPreferredWidth(10);
    }

    public void prikaziUpozorenje(String warningString) {
        JOptionPane.showMessageDialog(this, warningString, "Upozorenje", JOptionPane.WARNING_MESSAGE);
    }

    private void isprazniPolja() {
        jComboBoxIgraci.setSelectedItem(null);
        jComboBoxNapusteniKlubovi.setSelectedItem(null);
        jComboBoxPridruzeniKlubovi.setSelectedItem(null);
        stavkeUgovora = new ArrayList<>();
        postaviModelTabeleStavkaUgovora();
    }

